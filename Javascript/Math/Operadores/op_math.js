/*atalho para retirar um valor na variável, no caso abaixo a variável 'num' vale 5 novamente (6-1)*/
num--;
console.log("Valor de núm: " + num);

/*atalho para multiplicar um valor na variável, no caso abaixo a variável 'num' vale 10 (5*2)*/
